#include "die.h"
#include <cstdlib>

using namespace std;

die::die() {
    num = 1;
    srand(0);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int die::roll() {
    num = rand() % 6 + 1;
    return num;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int die::getNum() const {
    return num;
}